	<h4>Events</h4>
	<p>
	For one offs, or instances of a series, add individual <b>events</b>.  It's best to add as much data and fill out as many fields as possible, but the only required fields are <i>Name</i>, <i>Event Type</i> and <i>Start At</i>.
	Adding related entities and tags will help users discover your events.  But we would recommend not adding more than six tags, or excessively creating new tags that others users won't use.
